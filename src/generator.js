function pickRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)]
}

export function generatePosts(topic) {
  const count = 5
  const results = []
  const usedTitles = new Set()
  const usedTexts = new Set()

  for (let i = 0; i < count; i++) {
    let title
    do {
      title = pickRandom(titleTemplates)
    } while (usedTitles.has(title) && usedTitles.size < titleTemplates.length)
    usedTitles.add(title)

    let textIdx
    do {
      textIdx = textTemplates.indexOf(pickRandom(textTemplates))
    } while (usedTexts.has(textIdx) && usedTexts.size < textTemplates.length)
    usedTexts.add(textIdx)

    const formattedTitle = title.replace(/\{\{topic\}\}/g, topic)
    const formattedText = textTemplates[textIdx].map((s) => s.replace(/\{\{topic\}\}/g, topic))

    results.push({
      id: i + 1,
      title: formattedTitle,
      text: formattedText,
    })
  }
  return results
}